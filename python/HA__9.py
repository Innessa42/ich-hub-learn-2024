def is_pangram(sentence):
    # Определяем набор всех букв латинского алфавита
    alphabet = set('abcdefghijklmnopqrstuvwxyz')
    # Приводим строку к нижнему регистру и убираем пробелы
    sentence = sentence.lower().replace(' ', '')
    # Создаем множество букв из строки
    letters_in_sentence = set(sentence)
    # Проверяем, содержит ли множество букв из строки все буквы алфавита
    return alphabet.issubset(letters_in_sentence)

# Запрашиваем строку у пользователя
input_sentence = input("Введите строку: ")

# Проверяем, является ли строка панграммой
if is_pangram(input_sentence):
    print("Строка является панграммой.")
else:
    print("Строка не является панграммой.")

